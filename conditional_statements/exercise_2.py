"""
Ćwiczenie 5
Do zmiennej o nazwie number przypisz wartość 5.
Następnie wykorzystując instrukcję warunkową sprawdź,
czy zmienna number jest dodatnia, ujemna czy równa zero.
W każdym z tych przypadków wydrukuj do konsoli odpowiednio:
'The number 5 is positive.'
'The number 5 is negative.'
'The number 5 is equal to zero.'
Oczekiwany wynik:
The number 5 is positive.
"""
number = 5
if number > 0:
    print("The number 5 is positive.")
elif number < 0:
    print("The number 5 is negative.")
else:
    print("The number 5 is equal to zero.")

