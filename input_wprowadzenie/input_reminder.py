name = "Agnieszka"

print(name[0])
print(name[1])
print(name[8])

print(name[-1])
print(name[-2])

# name[start:stop]
print(name[1:4])

# name[:stop]
print(name[:4])

# name[start:]
print(name[2:])

print(name[-2:])

# name[start:stop:step]
full = "Agnieszka Kowalska"
print(full[10:])
print(full[::2])

numbers = "1,2,3,4,5"
print(numbers[::2])

print(numbers[::-1])
